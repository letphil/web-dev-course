// 1. Arithmetic Operators
let a = 10;
let b = 5;

console.log(a + b); // Addition
console.log(a - b); // Subtraction
console.log(a * b); // Multiplication
console.log(a / b); // Division
console.log(a % b); // Modulus

// 2. Assignment Operators
let c = 20;
c += 10; // Equivalent to c = c + 10
console.log(c);

c -= 5; // Equivalent to c = c - 5
console.log(c);

c *= 2; // Equivalent to c = c * 2
console.log(c);

c /= 5; // Equivalent to c = c / 5
console.log(c);

c %= 3; // Equivalent to c = c % 3
console.log(c);

// 3. Comparison Operators
let d = 15;
let e = 20;

console.log(d == e); // Equal to
console.log(d != e); // Not equal to
console.log(d > e); // Greater than
console.log(d < e); // Less than
console.log(d >= e); // Greater than or equal to
console.log(d <= e); // Less than or equal to

// 4. Logical Operators
let f = true;
let g = false;

console.log(f && g); // Logical AND
console.log(f || g); // Logical OR
console.log(!f); // Logical NOT

// 5. Unary Operators
let h = 10;

console.log(++h); // Increment
console.log(--h); // Decrement

// 6. Ternary Operator
let i = 25;
let result = i > 20 ? "Greater than 20" : "Less than or equal to 20";
console.log(result);
