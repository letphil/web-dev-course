// Exercise 1: Log a simple message
console.log("Hello, world!");

// Exercise 2: Log a number
console.log(42);

// Exercise 3: Log a boolean value
console.log(true);

// Exercise 4: Log a variable
let name = "Alice";
console.log(name);

// Exercise 5: Log multiple values
let age = 30;
console.log("Name:", name, "Age:", age);

// Exercise 6: Log an object
let person = {
  firstName: "John",
  lastName: "Doe",
  age: 25,
};
console.log(person);

// Exercise 7: Log an array
let fruits = ["Apple", "Banana", "Cherry"];
console.log(fruits);

// Exercise 8: Log a message with string interpolation
let city = "New York";
console.log(`I live in ${city}.`);

// Exercise 9: Log a message with concatenation
let country = "USA";
console.log("I live in " + city + ", " + country + ".");

// Exercise 10: Log a message with a function call
function greet(name) {
  return `Hello, ${name}!`;
}
console.log(greet("Bob"));
